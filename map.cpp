bool map::isinWall(float x, float y) {
    int xint = x/float(TILESIZE);
    int yint = y/float(TILESIZE);
    if(mapVector[yint][xint] != 0) {
        return true;
    } else {
        return false;
    }
}


void map::checkCol(float x, float y, float vx, float vy, bool &colX, bool &colY) {
    if(abs(vx) > abs(vy)) {
        if(vx*vy>0) {
            vy = vx;
        } else {
            vy = -vx;
        }
    } else {
        if(vx*vy>0) {
            vx = vy;
        } else {
            vx = -vy;
        }
    }
    colX = isinWall(x + 50*vx, y);
    colY = isinWall(x, y - 50*vy);
}


bool map::getline(float &x, float &y, float vx, float vy, float &horizontal, std::vector<std::vector<float>>& vec) {
    while(1) {
        if(isinWall(x+vx,y-vy)) {
            if(isinWall(x+vx,y)) {
                horizontal = 0;
            } else {
                horizontal = 1;
            }
            std::vector<float> temp;

            temp.push_back(x);
            temp.push_back(y);
            temp.push_back(horizontal);
            vec.push_back(temp);

            if(checkBlock((x + vx) * MAPSIZE / WINY, (y - vy) * MAPSIZE / WINY)%10==0) {
                return false;
            } else {
                return false;
            }
        } else {
            x += vx;
            y -= vy;
        }
    }
}

int map::checkBlock(int x, int y) {
    return mapVector[y][x];
}

SDL_Rect map::rayWall(int h, float pitch, int k, int ratio) {
    SDL_Rect rwall;
	rwall.x = WINY+k*(TILESIZE/8);
	rwall.w = TILESIZE/8;
    if(ratio==0) {
        rwall.y = (WINY/2-h/2)+pitch;
        rwall.h = h;
    } else {
        rwall.y = (WINY/2-h/2)+pitch+(10-ratio)*h/10;
        rwall.h = h*ratio/10;
    }
    return rwall;
}