bool map::getline(float &x, float &y, float vx, float vy, float &horizontal, std::vector<std::vector<float>> &vec)
{

    while (1)
    {

        if (isinWall(x + vx, y - vy))
        {
            if (isinWall(x + vx, y))
            {
                horizontal = 0;
            }
            else
            {
                horizontal = 1;
            }

            int blockX = (x + vx) * MAPSIZE / WINY;
            int blockY = (y - vy) * MAPSIZE / WINY;

            int m = checkBlock(blockX, blockY) % 10;

            if (vec.back()[3] != blockX || vec.back()[4] != blockY)
            {
                std::vector<float> temp;
                temp.push_back(x);
                temp.push_back(y);
                temp.push_back(horizontal);
                temp.push_back(blockX);
                temp.push_back(blockY);
                temp.push_back(0);
                temp.push_back(0);
                vec.push_back(temp);
            }

            if (m == 0)
            {
                return false;
            }
            else
            {
                x += vx;
                y -= vy;
                getline(x, y, vx, vy, horizontal, vec);
                return true;
            }
        }
        else
        {
            if (isinWall(x, y))
            {
                int blockXx = (x - vx) * MAPSIZE / WINY;
                int blockYy = (y + vy) * MAPSIZE / WINY;

                std::vector<float> tmp;
                tmp.push_back(x - vx);
                tmp.push_back(y + vy);
                tmp.push_back(horizontal);
                tmp.push_back(blockXx);
                tmp.push_back(blockYy);
                tmp.push_back(0);
                tmp.push_back(0);
                vec.push_back(tmp);
            }
            x += vx;
            y -= vy;
        }
    }
}

