bool map::isinWall(float x, float y)
{
    int xint = x / float(TILESIZE);
    int yint = y / float(TILESIZE);
    if (mapVector[yint][xint] != 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}

void map::checkCol(float x, float y, float vx, float vy, bool &colX, bool &colY)
{
    if (abs(vx) > abs(vy))
    {
        if (vx * vy > 0)
        {
            vy = vx;
        }
        else
        {
            vy = -vx;
        }
    }
    else
    {
        if (vx * vy > 0)
        {
            vx = vy;
        }
        else
        {
            vx = -vy;
        }
    }
    colX = isinWall(x + 50 * vx, y);
    colY = isinWall(x, y - 50 * vy);
}

bool map::getline(float &x, float &y, float vx, float vy, float &horizontal, std::vector<std::vector<float>> &vec)
{
    while (1)
    {
        if (isinWall(x + vx, y - vy))
        {
            if (isinWall(x + vx, y))
            {
                horizontal = 0;
            }
            else
            {
                horizontal = 1;
            }

            int blockX = (x + vx) * MAPSIZE / WINY;
            int blockY = (y - vy) * MAPSIZE / WINY;
            int m = checkBlock(blockX, blockY) % 10;

            if (vec.back()[3] != blockX || vec.back()[4] != blockY)
            {
                std::vector<float> temp;
                temp.push_back(x);
                temp.push_back(y);
                temp.push_back(horizontal);
                temp.push_back(blockX);
                temp.push_back(blockY);
                temp.push_back(0);
                temp.push_back(0);
                vec.push_back(temp);
            }

            if (m == 0)
            {
                return false;
            }
            else
            {
                x += vx;
                y -= vy;
                getline(x, y, vx, vy, horizontal, vec);
                return true;
            }
        }
        else
        {
            if (isinWall(x, y))
            {
                int blockXx = (x - vx) * MAPSIZE / WINY;
                int blockYy = (y + vy) * MAPSIZE / WINY;

                std::vector<float> tmp;
                tmp.push_back(x - vx);
                tmp.push_back(y + vy);
                tmp.push_back(horizontal);
                tmp.push_back(blockXx);
                tmp.push_back(blockYy);
                tmp.push_back(0);
                tmp.push_back(0);
                vec.push_back(tmp);
            }
            x += vx;
            y -= vy;
        }
    }
}

int map::checkBlock(int x, int y)
{
    return mapVector[y][x];
}

SDL_Rect map::rayWall(float pitch, int o, int ratio, std::vector<std::vector<float>> &vec)
{
    SDL_Rect rwall;
    rwall.x = angleDiffFix(o);
    rwall.w = TILESIZE / 8;
    if (ratio == 0)
    {
        rwall.y = (WINY / 2 - vec.back()[5] / 2) + pitch;
        rwall.h = vec.back()[5];
    }
    else
    {
        vec.back()[6] = (WINY / 2 - vec.back()[5] / 2) + pitch + (10 - ratio) * vec.back()[5] / 10;
        rwall.y = vec.back()[6];
        rwall.h = vec.back()[5] * ratio / 10;
    }
    return rwall;
}

short map::angleDiffFix(int o)
{
    float ray = FOV * (floor(0.5f * WINY) - o) / (WINY - 1);
    float rayidk = 0.5f * tan(ray * (M_PI / 180)) / tan(0.5f * FOV * M_PI / 180);
    short posit = static_cast<short>(round(WINY * (0.5f - rayidk)));
    return posit + WINY;
}